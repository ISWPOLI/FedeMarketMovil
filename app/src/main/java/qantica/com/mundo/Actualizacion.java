package qantica.com.mundo;

public class Actualizacion {

    String nombre;
    String descripcion;
    int id, descarga;
    double rating;
    String version;
    String estado;
    String cap_1, cap_2, ico;

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getDescarga() {
        return descarga;
    }

    public void setDescarga(int descarga) {
        this.descarga = descarga;
    }

    public double getRating() {
        return rating;
    }

    public void setRating(double rating) {
        this.rating = rating;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getCap_1() {
        return cap_1;
    }

    public void setCap_1(String cap_1) {
        this.cap_1 = cap_1;
    }

    public String getCap_2() {
        return cap_2;
    }

    public void setCap_2(String cap_2) {
        this.cap_2 = cap_2;
    }

    public String getIco() {
        return ico;
    }

    public void setIco(String ico) {
        this.ico = ico;
    }


}
